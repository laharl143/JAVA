public class Form {
    public static void main(String[] args) {
        char gender = 'F';                      //Char is used for single Letters ; it is also important to use single quotation marks for char. if you use double quotation mark, it will lead to error.
        System.out.println("Gender: " + gender);
    }
}


// FAQS:
// Why use char?
// Memory: char consumes less memory
// Performance: char is faster than string

// Rule of thumb:
// Use char to store single characters
// Use String to store text