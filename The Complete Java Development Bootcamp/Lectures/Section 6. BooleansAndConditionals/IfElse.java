public class IfElse {
    public static void main(String[] args) {
        int grade = 45;

        if (grade > 50) {                //if the statement is false, it will skip the "if" statement and proceed to else
            System.out.println("Great Job, You passed!");  
        } else {
            System.out.println("Try again next time..");
        }
    }

}
